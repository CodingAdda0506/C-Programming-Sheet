// Write a program to display first ten natural numbers
#include <stdio.h>
#include <conio.h>
void main()
{
    printf("First 10 Natural Numbers are: \n");
    // Iterate from 1 to 10 using for loop
    for (int i = 1; i <= 10; i++)
    {
        // print each iteration
        printf("%d\n", i);
    }
}